	<h2>Advocating for divestment:</h2>
	<li>Support the UC Regent divestment petition</li>
	<div>This has precedent, namely in the UC’s divestment from South African apartheid.</div>
	<li>Supporting student free speech and demonstration</li>
	<li>
		The UC regents have proposed a <a
			href="https://regents.universityofcalifornia.edu/regmeet/jan24/j3attach1.pdf">new policy</a
		> forbidding UC employees from speaking through university channels about their political opinions,
		but allows high-ranking UC members to continue to share their opinions. This allows for the UC system
		as a whole to form a central narrative on issues, and prevents professors, GSIs, and other staff
		from disagreeing and speaking their mind. We must work to ensure that this does not get passed.
	</li>

